package pack01.exif;

public class Test03_If_char {
	public static void main(String[] args) {
		char char1 = 'A';

		if (char1 == 'A') {

			System.out.println('A');
		} else if (char1 == 'B') {
			System.out.println('B');

		} else if (char1 == 'o') {
			System.out.println('o');

		} else {
			System.out.println("혈액행과 같지않다");

		}
	}
}